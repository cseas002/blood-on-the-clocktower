export const allCharacters: Character[] = [
  ...troubleBrewingCharacters,
];

export const getCharacterById = (id: string): Character | undefined => {
  return allCharacters.find(char => char.id === id);
};

export const getCharactersByEdition = (edition: string): Character[] => {
  return allCharacters.filter(char => char.edition === edition);
};

export const getCharactersByTeam = (team: string, edition?: string): Character[] => {
  let characters = allCharacters.filter(char => char.team === team);
  if (edition) {
    characters = characters.filter(char => char.edition === edition);
  }
  return characters;
};
